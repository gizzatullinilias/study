class Cat:
    def __init__(self, name, color, age):
        self.name = name
        self.color = color
        self.age = age

    def meow(self):
        print(self.name + ": Meow!")

    def purr(self):
        print(self.name + ": Purr...")

    def sleep(self):
        print(self.name + ": Zzzzz...")


